                                    <h4 class="entry-title">ميزات الوجهات اليو بي في سي</h4>
                                    <p>مقاومة عالية للمياه والأمطار 
                                      إن خام الـ يو بي في سي بطبيعته لايمتص ويتشرب الماء بدرجة 100% ، كما أن التصميمات الفريدة لفتحات خطوط التصريف توفر تلك القطاعات حماية كاملة ضد المياه أن الإختبارات التي أجريت على مستويات متعددة من الضغوط أظهرت عدم وجود أي ترشيح عند الإختبار على 4 لتر/دقيقة لكل م2 لمدة عشرة دقائق 
                                      
                                      مقاومة الصدمات 
                                      إن قطاعات الـ يو بي في سي صممت بطريقة وتركيبة خاصة لتمكنها من مقاومة الصدمات، حيث أنها تتحمل قوة الصدمات بمعدل 3 كجم/1 مم 
                                      
                                      مقاومة للأحماض والأكسدة 
                                      إن منتجات الـ يو بي في سي ل تتأثر بعوامل الأكسدة والقلويات والغازات النشطة والأملاح حيث لديها مقاومة عالية لعوامل التآكل عند إستخدامها في البيئة المحيطة بالدول الخلیجیة وعواصفها الصحراوية ولمقاومة التعرض لتلوث الصناعات الثقيلة حيث أن نوافذ الـ يو بي في سي أثبتت فعاليتها المؤكدة في المصانع والعامل خاصة في مصانع الغزل والورق والمطاحن 
                                      
                                      
                                      مقاومة تامة للعوامل الجوية 
                                      لقد تم تشكيل قطاعات مارليك لنوافذ الـ يو بي في سي بمواصفات خاصة بحيث تحقق مقاومة عالية ضد عوامل التعرية الجوية حيث يمكنها الصمود أمام التعرض للمناخ القاري تحت الشمس المحرقة، والعواصف، والأمطار، والرطوبة العالية حيث يمكن إستخدامها أكثر من خمسين عام دون اي يحدث بها أي خدوش أو تشققات بعد تلك الفترة 
                                      
                                      
                                      
                                      حماية مؤكدة من الضوضاء 
                                      إن مادة الـ يو بي في سي العازلة للصوت بطبيعتها قد تضافرت مع تصيمات الفراغات الاخلية بقطاعات نوافذ الـ يو بي في سي والسيور المطاطية المحكمة الإغلاق لتوفير أفضل عزل للأصوات وخفض الضوضاء لأقل حد ممكن حيث وصل عزل الأصوات إلى (22-25) ديسبل في النوافذ دات الزجاج المزدوج، كما أنها توفر فرصة الهدوء والسكينة مما جعلها مفضلة الإستخدام لدى المستشفيات، المدارس، المباني الإارية وعلاوة على ذلك فإنها توفر لك بعد عناء يوم من العمل فرصة الإستمتاع بالهدوء والإسترخاء بعيدا عن الضوضاء في الخارج 
                                      
                                      عزل ممتاز للحرارة وتوفير الطاقة 
                                      في المباني المكيفة الهواء تستطيع النوافذ العازلة الإقلال من الطاقة المفقودة بنسبة 37% تقريبا . 
                                      إن الإختيار الذكي لنوعية المواد المستخدمة في تصنيع النوافذ وإختيار الزجاج هي العوامل الرئيسية لتحديد جودة العزل. إن معامل إنتقال الحرارة في مواد الـ يو بي في سي تصل إلى 13 كالوري في المتر المربع (2م/13ك) وهي تمثل 1/1250 من معامل إنتقال الحرارة خلال الألمنيوم والذي يبلغ إنتقال الحرارة له 175 في المتر المربع إن نوافذ الـ يو بي في سي المنزلقة تصل معامل إنتقال الحرارة خلالها إلى 6,3 كالوري/م2 على أنه في نوافذ الألمنيوم يبلغ 16,3 كالوري/م2 (هذه القيم تم الحصول عليها من الإختبارات الفعلية للنوافذ) لذا فإنها تعتبر من أحسن النوافذ في مجال ترشيد الطاقة 
                                      دقة عالية في إحكام الغلق 
                                      إن مادة الـ يو بي في سي يمكن تشغيلها وضبط مقاساتها بدرجة عالية من الدقة والإحكام حيث أن فرق القياس في الطول أو العرض لا تتجاوز +/-3مم مما يضمن إحكام غلق نوافذ الـ يو بي في سي 
                                      أقل تسرب للهواء 
                                      إن تصميم العوازل الجوية يوفر الحد الأقصى لضمان عدم تسرب لكل متر طولي يصل إلى 8م3 وذلك بالنسبة للنوافذ المنزلقة و2م3 للطرازات الأخرى. إن تخفيض كمية الهواء المتسرب يؤدي بالتالي إلى زيادة الترشيد وتوفير إستهلاك الطاقة المستخدمة في أجهزة التكييف 
                                      المظهر الجذاب 
                                      إن نوافذ الـ يو بي في سي تتميز بسطحها الناعم المستوى أركانه وزواياها المصقولة بالإضافة إلى ألوانها الزاهية التي تضفي المظهر الجذاب لتلك النوافذ 
                                      
                                      حماية كاملة من أخطار الحريق 
                                      إن مادة الـ يو بي في سي توفر الحماية الكالة ضد الحريق وذلك وفقا للمواصفات رقم 4102 داين وذلك نظرا لعدم قابيلتها للإشتعال حيث تتميز بخاصية للإطفاء الذاتي. ووفقا لنتائج إحتراق المواد، الأخشاب، الألمنيوم والـ يو بي في سي والتي أجريت بمعرفة إطفائية زيروخ بألمانيا في 19 فبراير 1973 فإن نوافذ الـ يو بي في سي قد أثبتت فعاليتها في عدم تلفها وفي الوفاء باشتراطات الدفاع المدني بالنسبة لمواد يمكن إستخدامها في جميع أنماط المباني بغض النظر عن شرط الإرتفاعات في البلدان الأخرى وتم إعتمادها وتعريفها مادة فعالة ومؤمونة الإسخدام من قبل هيئات الدفاع المدني ومكافحة الحريق في النمسا وألمانيا ونيوزلندا وسويسرا والولايات المتحدة 
                                      
                                      قدرة عالية لعزل الكهرباء 
                                      إن مواد الـ يو بي في سي تتميز بقدرتها العالية على عزل الكهرباء التي تزيد عن 15-10 أوم/سم مما يجعلها مأمونة تماما من أي تأثير للكهراباء 
                                      
                                      انخفاض معدل التمدد الحراري 
                                  إن نوافذ الـ يو بي في سي تعمل بسهولة وكفاءة تامة تحت جميع الظروف الجوية وذلك نظرا لعدم تأثرها بالتمدد الحراري (3x10.5degc/cm) حيث أنه في فصل الصيف حين تكون الحرارة في الخارج 45 درجة مئوية فإن أقصى حد للتمدد الحراري يصل إلى 0,33مم لكل 1,5 متر من القطاع، أما في فصل الشتاء فإن أقصى حد للتمدد الحراري يصل إلى 0,9مم لكل 1,5 متر من طول قطاعات الـ يو بي في سي </p>
                                    
